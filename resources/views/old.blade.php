<section class="bg-primary" id="geschichte">
    <div class="container">
        <div class="row">
            <div class="col-lg-8 col-lg-offset-2 text-center">
                <br><br>
                <h2 class="section-heading">Unsere Geschichte</h2>
                <hr class="light">
                <p class="text-faded">Hervorgegangen aus dem 1896 gegründeten "Akademisch-Pharmazeutischen Kegelklub Carolo-Willhelmina" trat die Sängerschaft 1900 als "Akademisch-Pharmazeutischer Verein" ins Leben. Der Zirkel weist bis heute mit seinen Buchstaben "A", "Ph", und "V" noch auf die Entstehung hin.
                    Mit dem Beitritt zum "Arnstädter Cartellverband" im Jahre 1909 nahm die Verbindung den Namen "Arminia" an. Die Zugehörigkeit zu diesem Verband endete 1913. Ein Jahr später, im Jahre 1914, trat die Arminia dem SV bei. Neben der Tatsache, dass die Verbindung nichtfarbentragend war, trug auch der Einfluss des SV-Ortsverbandes Braunschweig zu diesem Schritt bei.
                    Nach dem ersten Weltkrieg konnte die musikalische Arbeit in der Verbindung, die zunehmend Angehörige aller Fakultäten umfasste, intensiviert werden.
                    Der Auflösung der Korporationen fiel 1935 auch die Arminia zum Opfer. Der damalige Vorstand des Altherrenverbandes konnte den Bund aber im geheimen weiter zusammenhalten, wozu auch das Weitererscheinen des 1924 gegründete "Arminenblattes" bis 1944 beitrug. Einige Erinnerungsstücke, unter anderem auch die Arminen-Fahne, befinden sich heute in der Erinnerungsstätte des SV in Sondershausen.
                    Nach dem Krieg fand 1949 in Vlotho das erste große Arminentreffen statt. Im Januar 1950 wurde die Verbindung zunächst als "Akademisch-Musikalische" wieder zur Hochschule zugelassen, nahm im Juli 1950 wieder den alten Namen "Arminia" an und 1952 dann die Bezeichnung "Sängerschaft". Seit 1973 lautet der offizielle Name "Akademisch-Musische Vereinigung (AMV) Arminia".
                    1954 erfolgte die Einweihung des ersten Arminenheims in der Spielmannstraße, 1977 bezog die Arminia ihr jetziges Domizil. 1987 entschloss sich die Aktivitas, auch Frauen aufzunehmen und gründete 1989 den "Verein Philistrierter Arminen (VPA). 1989 sowie zuletzt 2013 stellte die Arminia den Vorsitz des Sondershäuser Verbands."</p>
